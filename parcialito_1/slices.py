def primeros_dos_c(cadena):
    '''
    DOC: Completar.
    '''
    return cadena[:2]


def ultimos_tres_c(cadena):
    '''
    DOC: Completar.
    '''
    return cadena[-3:]


def cada_dos_c(cadena):
    '''
    DOC: Completar.
    '''
    return cadena[::2]


def sentido_inverso(cadena):
    '''
    DOC: Completar.
    '''
    return cadena[::-1]


def normal_e_inversa(cadena):
    '''
    DOC: Completar.
    '''
    return cadena + cadena[::-1]